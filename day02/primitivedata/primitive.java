package day02.primitivedata;

public class primitive {
    public static void main(String[] args) {
        byte byteNumber = 30;
        System.out.println(byteNumber);

        short shortNumber = 1000;
        System.out.println("My short number is: " + shortNumber);

        int intNumber = 3000;
        System.out.println("My int number is: " + intNumber);

        long longNumber = 320000L;
        System.out.println("My long number is: " + longNumber);

        float floatNumber = 32.99f;
        System.out.println("My float number is: " + floatNumber);

        double doubleNumber = 27.99;
        System.out.println("My double number is: " + doubleNumber);

    }
}
